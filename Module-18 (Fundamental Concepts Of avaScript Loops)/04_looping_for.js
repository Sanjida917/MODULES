//Even number

for (let num2 = 1; num2 <= 20; num2++) {

    if (num2 % 2 == 0) {

        console.log("Even Number : ", num2);
    }

}

//Odd Number
for (let num3 = 1; num3 <= 20; num3++) {

    if (num3 % 2 != 0) {

        console.log("Even Number : ", num3);
    }

}

//sum 11-20

